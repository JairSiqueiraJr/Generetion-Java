package ExerciciosHeranca;

public class Animal {
	
	private String nome;
	private int idade;
	private String sons;
	private String dever;
	
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	public String getSons() {
		return sons;
	}
	public void setSons(String sons) {
		this.sons = sons;
	}
	public String getDever() {
		return dever;
	}
	public void setDever(String dever) {
		this.dever = dever;
	}
	
}
